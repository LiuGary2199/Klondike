using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class FormalGate
{
    public static string EmbryoBeSow(double a)
    {
        return Math.Round(a, UnfoldAdjoin.RoundAttend).ToString();
    }
    public static string EmbryoBeSow(double a, int digits)
    {
        return Math.Round(a, digits).ToString();
    }

    public static double Amino(double a)
    {
        return Math.Round(a, UnfoldAdjoin.RoundAttend);
    }

}
